// Problem Description
/*
 Write a program to print all Natural numbers from 1 to N where you have to take N as input from user
 */

/*Problem Constraints
 1 <= N <= 10000000
 */

//Input Format
//A single line representing N

//Output Format
//N space separated integers from 1 to N.

//YOUR CODE GOES HERE
//Swift

let n = Int(readLine()!)
var a = 0
while(a < n!){
    a = a + 1
    print( a,"", separator: " ", terminator:"")
}

//Example Input
//Input 1:
//5

//Input 2:
//10

////Example Output
//Output 1:
